export const getFeaturedNovels = (): Novel[] => novels.filter(n => n.isFeatured);
export const getNewNovels = (): Novel[] => novels.filter(n => n.isNew);
export const getTopNovels = (limit: number = 10): Novel[] => 
  [...novels].sort((a, b) => b.views - a.views).slice(0, limit);
export const getNovelsByCategory = (category: string): Novel[] => 
  novels.filter(n => n.categories.includes(category));
export const getNovelById = (id: string): Novel | undefined => 
  novels.find(n => n.id === id);
export const getNovelsByAuthor = (authorId: string): Novel[] => 
  novels.filter(n => n.author.id === authorId);
export const searchNovels = (query: string): Novel[] => 
  novels.filter(n => 
    n.title.toLowerCase().includes(query.toLowerCase()) ||
    n.synopsis.toLowerCase().includes(query.toLowerCase()) ||
    n.author.name.toLowerCase().includes(query.toLowerCase()) ||
    n.tags.some(t => t.toLowerCase().includes(query.toLowerCase()))
  );
export const getNovelsWithVideo = (): Novel[] => 
  novels.filter(n => n.youtubeVideoId);
